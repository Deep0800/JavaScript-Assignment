//ii. Vowel or Consonant using switch case in JS

let char = "G"

switch (char) {
    case "A":
    case "a":
    case "E":
    case "e":
    case "I":
    case "i":
    case "O":
    case "o":
    case "U":
    case "u":
        console.log("Vowel");
        break;
    default:
        console.log("Consonant");
        break;
}